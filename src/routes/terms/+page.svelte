        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제1조 (목적)</h2>
            <p class="text-gray-700 leading-relaxed">
                이 약관은 부평동부교회 홈페이지에서 제공하는 모든 자료의 이용에 관한 조건 및 절차, 교회와 회원 간의 권리·의무·책임사항을 규정합니다.
            </p>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제2조 (이용약관의 명시, 효력 및 개정)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>약관 내용은 서비스 초기 화면에 게시합니다.</li>
                <li>관련법을 위배하지 않는 범위에서 개정할 수 있습니다.</li>
                <li>개정 시 기존약관과 개정약관, 적용일자 및 사유를 명시하여 적용일 15일 전(불리한 경우 30일 전)부터 공지합니다.</li>
                <li>개정약관 시행일 7일 후까지 거부 의사를 표시하지 않으면 승인으로 간주합니다.</li>
                <li>회원이 개정약관에 동의하지 않으면 이용계약을 해지할 수 있습니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제3조 (정의)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li><strong>서비스</strong>: 단말기 구분 없이 회원이 이용 가능한 교회의 모든 관련 서비스</li>
                <li><strong>회원</strong>: 약관에 따라 이용계약을 체결하고 서비스를 이용하는 고객</li>
                <li><strong>아이디</strong>: 회원이 정하고 교회가 승인하는 문자와 숫자 조합</li>
                <li><strong>비밀번호</strong>: 회원이 선정한 문자와 숫자 조합</li>
                <li><strong>게시물</strong>: 회원이 서비스상에 게시한 부호, 문자, 음성, 음향, 동영상, 파일, 링크 등</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제4조 (이용계약 체결)</h2>
            <p class="text-gray-700 leading-relaxed mb-3">신청자의 동의 및 가입신청과 교회의 승낙으로 체결됩니다. 교회는 원칙적으로 승낙하되, 다음의 경우 거절할 수 있습니다.</p>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed mb-3">
                <li>실명이 아니거나 타인 명의를 사용한 경우</li>
                <li>허위 정보를 기재하거나 허위서류를 첨부한 경우</li>
                <li>14세 미만 아동이 부모 동의를 획득하지 않은 경우</li>
                <li>규정을 위반하여 신청한 경우</li>
            </ul>
            <p class="text-gray-700 leading-relaxed">설비 부족, 기술상 지장, 정보도용차단요청 등의 경우 승낙을 유보할 수 있으며, 거절·유보 시 신청자에게 고지합니다. 이용계약은 교회가 가입완료를 인정한 시점에 성립합니다.</p>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제5조 (아이디 등의 관리책임)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>회원이 아이디와 비밀번호 관리책임을 부담합니다.</li>
                <li>제3자 이용으로 발생한 불이익은 회원이 부담합니다.</li>
                <li>교회는 개인정보 유출 우려, 반사회적 아이디 이용을 제한할 수 있습니다.</li>
                <li>도난·부정 사용 인지 시 즉시 비밀번호를 수정하고 교회에 통보해야 합니다.</li>
                <li>미통보 또는 미조치로 발생한 불이익에 대해 교회는 책임지지 않습니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제6조 (개인정보보호 의무)</h2>
            <p class="text-gray-700 leading-relaxed">
                교회는 정보통신망법 등 관계법령에 따라 개인정보를 보호하며, 개인정보취급방침을 준수합니다. 단, 공식 사이트 외 링크 사이트에는 적용되지 않습니다.
            </p>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제7조 (회원정보의 변경)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>회원은 회원정보관리에서 개인정보를 열람·수정할 수 있습니다. (실명, 아이디 제외)</li>
                <li>변경사항은 회원 본인이 직접 변경하는 것을 원칙으로 합니다.</li>
                <li>변경사항을 온라인 또는 이메일로 통보해야 하며, 관리자 수정 시 본인 증명 자료를 제출해야 합니다.</li>
                <li>미통보로 발생한 불이익에 대해 교회는 책임지지 않습니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제8조 (회원에 대한 통지)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>서비스 변경, 관리·운영상 통지는 이메일로 할 수 있습니다.</li>
                <li>불특정 다수에 대한 통지는 게시판·공지사항으로 갈음할 수 있습니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제9조 (교회의 의무)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>지속적·안정적 서비스 제공을 위한 유지·점검·복구를 성실히 이행합니다.</li>
                <li>회원의 안전한 서비스 이용을 위해 보안시스템을 구축하고 개인정보취급방침을 공시·준수합니다.</li>
                <li>제3자에게 서비스를 제공할 경우 명시적 동의 후 관련 법령을 준수합니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제10조 (회원의 의무)</h2>
            <p class="text-gray-700 leading-relaxed mb-3">회원은 다음 행위를 해서는 안 됩니다.</p>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed mb-3">
                <li>신청·변경 시 허위 내용 등록</li>
                <li>타인 정보 도용</li>
                <li>교회 서비스 정보를 사전 승낙 없이 복제, 출판, 방송, 영리·비영리 목적 사용 또는 제3자 제공</li>
                <li>저작권 등 지적재산권 침해</li>
                <li>교회·제3자 명예 손상 또는 업무 방해</li>
                <li>외설, 폭력적 메시지, 이단 관련 정보 공개·게시</li>
                <li>타인 개인정보를 동의 없이 수집·저장·공개</li>
                <li>불법적·부당한 행위</li>
                <li>법령, 약관, 서비스 규정 위반</li>
            </ul>
            <p class="text-gray-700 leading-relaxed">교회는 위반 회원에 대해 게시물 삭제·임시 삭제, 서비스 이용 제한, 계약 일방 해지 조치를 취할 수 있습니다.</p>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제11조 (서비스의 변경 및 중지)</h2>
            <p class="text-gray-700 leading-relaxed mb-3">교회는 다음의 경우 서비스를 변경·중지할 수 있습니다.</p>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed mb-3">
                <li>회원의 고의·과실로 운영이 방해되는 경우</li>
                <li>제10조의 의무를 위반한 경우</li>
                <li>설비 점검, 보수, 공사</li>
                <li>기간통신사업자의 서비스 중지</li>
                <li>국가비상사태, 설비 장애, 이용 폭주</li>
                <li>기타 중대한 사유로 제공이 부적당하다고 판단되는 경우</li>
            </ul>
            <p class="text-gray-700 leading-relaxed">사유·제한기간 등을 회원에게 고지해야 하며, 변경 시 변경사유, 내용, 제공일자를 변경 전에 게시합니다. 무료 서비스는 수정·중단·변경할 수 있으며 별도 보상은 없습니다.</p>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제12조 (정보의 제공)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>교회는 공지사항, 이메일 등으로 정보를 제공할 수 있습니다.</li>
                <li>회원은 거래 관련 정보·고객문의 답변을 제외하고 이메일 수신을 거절할 수 있습니다.</li>
                <li>이용자는 교회의 게시물·정보를 변경·수정·제한할 수 없습니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제13조 (권리의 귀속 및 저작물의 이용)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>게시물의 저작권은 저작권자에게 귀속됩니다.</li>
                <li>게시물은 검색결과·서비스에 노출될 수 있으며, 필요 범위 내에서 수정·복제·편집될 수 있습니다. (저작권법 준수, 회원은 삭제·비공개 조치 가능)</li>
                <li>위 범위 외 이용 시 사전 동의가 필요합니다.</li>
                <li>서비스의 저작권·지적재산권은 교회에 귀속됩니다. (회원 게시물·제휴계약 저작물 제외)</li>
                <li>교회는 이용조건에 따른 이용권만 부여하며, 회원은 이를 양도·판매·담보제공할 수 없습니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제14조 (이용계약의 해제 및 해지)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>회원은 언제든 고객센터·회원정보관리에서 해지 신청을 할 수 있으며, 교회는 즉시 처리합니다.</li>
                <li>제10조 위반 시 교회가 일방적으로 해지할 수 있으며, 손해 발생 시 민·형사 책임이 발생할 수 있습니다.</li>
                <li>해지 시 관련법·개인정보취급방침에 따른 보유 경우를 제외하고 모든 데이터가 소멸합니다.</li>
                <li>타인이 담기·스크랩한 게시물이나 공용게시판 등록물은 삭제가 보장되지 않으므로 사전에 삭제하시기 바랍니다.</li>
            </ul>
        </section>

        <section>
            <h2 class="text-xl font-bold text-gray-900 mb-3">제15조 (면책)</h2>
            <ul class="list-disc list-inside space-y-2 text-gray-700 leading-relaxed">
                <li>천재지변 등 불가항력으로 서비스 제공이 불가한 경우 책임을 면합니다.</li>
                <li>회원의 귀책사유로 인한 장애에 대해서는 책임지지 않습니다.</li>
                <li>게시된 정보·자료·사실의 신뢰도·정확성에 대해 책임지지 않습니다.</li>
                <li>회원 간, 회원과 제3자 사이의 거래에 대해 책임지지 않습니다.</li>
                <li>무료 서비스 이용과 관련하여 법령에 규정이 없는 경우 책임지지 않습니다.</li>
            </ul>
        </section>

        <section>
